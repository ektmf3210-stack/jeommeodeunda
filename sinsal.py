def analyze(팔자, ilgan):
    """팔자({'년주':'丙子(병자)',...}) + 일간 -> [(살이름, 쉬운설명), ...]"""
    ju = ["년주", "월주", "일주", "시주"]
    ganji = {p: 팔자[p][:2] for p in ju}          # '丙子'
    jiji = [팔자[p][1] for p in ju]                # ['子','巳','酉','辰']
    ilji, yeonji = 팔자["일주"][1], 팔자["년주"][1]
    found = []

    gj = CHUNEUL.get(ilgan, "")
    if any(z in gj for z in jiji):
        found.append("천을귀인")
    for ref in {ilji, yeonji}:
        guk = SAMHAP.get(ref)
        if not guk:
            continue
        if YEOKMA[guk] in jiji:
            found.append("역마살")
        if DOHWA[guk] in jiji:
            found.append("도화살")
        if HWAGAE[guk] in jiji:
            found.append("화개살")
    if YANGIN.get(ilgan) in jiji:
        found.append("양인살")
    if MUNCHANG.get(ilgan) in jiji:
        found.append("문창귀인")
    if HONGYEOM.get(ilgan) in jiji:
        found.append("홍염살")
    for p in ju:
        if ganji[p] in BAEKHO:
            found.append("백호살")
        if ganji[p] in GWAEGANG:
            found.append("괴강살")

    seen, out = set(), []
    for name in found:
        if name not in seen:
            seen.add(name)
            out.append((name, DESC[name]))
    return out


