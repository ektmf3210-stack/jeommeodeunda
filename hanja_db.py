def chosung(ch):
    """한글 한 글자 -> 초성 문자."""
    code = ord(ch) - 0xAC00
    if 0 <= code <= 11171:
        return CHOSEONG[code // 588]
    return ""


def eum_ohaeng(ch):
    """한글 한 글자 -> 발음오행."""
    return CHO_OHAENG.get(chosung(ch), "")


