def cyclic_branch_to_month(zhi):
    m = {"寅":"2~3월경","卯":"3~4월경","辰":"4~5월경","巳":"5~6월경","午":"6~7월경","未":"7~8월경",
         "申":"8~9월경","酉":"9~10월경","戌":"10~11월경","亥":"11~12월경","子":"12~1월경","丑":"1~2월경"}
    return m.get(zhi, zhi)


def full_reading(year, month, theme_gate="生門", day=15):
    """특정 월 반의 주제 자리(theme_gate)를 100% 해석해 현대어 문장 리스트로 반환."""
    p = build_qimen(*[(lambda b: (b, b["절기"]))(get_bazi(datetime(year, month, day, 12, 0)))][0])
    g = p["궁위순서"]
    i = p["팔문"].index(theme_gate)
    gung = g[i]
    mun, star, god = p["팔문"][i], p["구성"][i], p["팔신"][i]
    ch, ji = p["천반간"][i], p["지반간"][i]

    lines = {}
    lines["방위"] = DIRK[gung]
    lines["성격"] = GATE_STYLE.get(mun, "")
    lines["기운"] = STAR_FORCE.get(star, "")
    lines["변수"] = GOD_VAR.get(god, "")

    # 삼기 겹침 (길한 기운)
    sanqi_here = [x for x in (ch, ji) if x in SANQI_MEAN]
    if len(sanqi_here) >= 2:
        lines["삼기"] = f"이 자리에 아주 길한 기운({'·'.join(SANQI_MEAN[x] for x in sanqi_here)})이 둘이나 겹쳤어. 이런 건 흔치 않아 — 놓치면 안 되는 결정적 자리라는 뜻이야"
    elif len(sanqi_here) == 1:
        lines["삼기"] = f"이 자리에 길한 기운({SANQI_MEAN[sanqi_here[0]]})이 함께해 흐름이 좋아"
    else:
        lines["삼기"] = ""

    # 값부 = 주도권 위치
    zf_star, zf_gung = p["값부낙궁"]
    lines["주도권"] = ("이 일의 주도권이 너한테 있어(중심 자리)" if zf_gung == "中五宮"
                    else f"주도권의 핵심은 {DIRK.get(zf_gung,'')} 쪽 기운에 달려 있어")

    # 공망 = 헛수고 구간
    kw = p["공망"]
    lines["헛수고구간"] = f"단, {cyclic_branch_to_month(kw[0])}·{cyclic_branch_to_month(kw[1])}는 기운이 비는 구간이야. 이때 서두르면 헛발질이 되기 쉬우니 힘을 아껴"

    # 역마 = 이동수 (역마 지지 → 방위)
    YEOKMA_DIR = {'寅':'동북','午':'정남','戌':'서북','申':'서남','子':'정북','辰':'동남',
                  '巳':'동남','酉':'정서','丑':'동북','亥':'서북','卯':'정동','未':'서남'}
    ym_dir = YEOKMA_DIR.get(p['역마'], '')
    lines["이동수"] = (f"이동·변화의 기운이 {ym_dir}쪽에 붙어 있어. 이사·이직·출장처럼 움직임이 필요한 일이라면 그 방향이 열쇠야"
                    if ym_dir else "")

    return lines


