# 길흉 등급 (전통 81수 분류)
CHOISANG = {13, 16, 21, 23, 31, 41}                       # 최상운수
SANG = {1, 3, 5, 6, 11, 15, 18, 24, 32, 35, 37, 39}       # 상운수
YANG = {7, 8, 17, 25, 29, 33, 38, 45, 47, 48, 52, 57, 58,
        61, 63, 65, 67, 68, 71, 73, 75, 77, 81}           # 양운수(길)
HYUNG = {2, 4, 12, 14, 27, 28, 30, 53, 59, 60, 62, 69, 72}  # 흉운수
CHOIHYUNG = {9, 10, 19, 20, 22, 26, 34, 36, 40, 42, 43, 44,
             46, 50, 54, 56, 64, 66, 70, 74, 78, 80}        # 최흉운수
NANBUN = {49, 51, 55, 76, 79}                              # 길흉 난분(작명에선 피함)


def _reduce(n):
    """81 초과 시 80을 빼서 1~81 범위로."""
    while n > 81:
        n -= 80
    return n if n >= 1 else 1


def grade(n):
    """획수 합 -> ('최상'|'상'|'길'|'흉'|'최흉', 격이름)."""
    n = _reduce(n)
    name = GYEOK.get(n, "")
    if n in CHOISANG:
        return "최상", name
    if n in SANG:
        return "상", name
    if n in YANG:
        return "길", name
    if n in CHOIHYUNG:
        return "최흉", name
    if n in HYUNG or n in NANBUN:
        return "흉", name
    return "흉", name


def is_gil(n):
    n = _reduce(n)
    return n in CHOISANG or n in SANG or n in YANG


def four_gyeok(seong_hoek, name_hoeks):
    """성 획수(int) + 이름 글자 획수(list, 보통 2개) -> 사격 dict.
    원격=이름합, 형격=성+이름첫, 이격=성+이름끝, 정격=성+이름전체.
    """
    a = name_hoeks[0]
    b = name_hoeks[-1]
    won = sum(name_hoeks)                 # 원격(초년)
    hyeong = seong_hoek + a               # 형격(청년)
    i = seong_hoek + b                    # 이격(장년)
    jeong = seong_hoek + sum(name_hoeks)  # 정격(총운)
    out = {}
    for key, val, kr in [("원격", won, "초년운"), ("형격", hyeong, "청년운"),
                         ("이격", i, "장년운"), ("정격", jeong, "말년·총운")]:
        g, gname = grade(val)
        out[key] = {"수": val, "등급": g, "격": gname, "뜻": kr, "길": is_gil(val)}
    out["_모두길"] = all(out[k]["길"] for k in ("원격", "형격", "이격", "정격"))
    out["_길개수"] = sum(out[k]["길"] for k in ("원격", "형격", "이격", "정격"))
    return out


def four_gyeok_single(seong_hoek, name_hoek):
    """외자(홑이름) 사격. 원격=이름 단독, 정격=성+이름. (형/이격은 외자 특성상 생략)"""
    won = name_hoek
    jeong = seong_hoek + name_hoek
    out = {}
    for key, val, kr in [("원격", won, "초년운"), ("정격", jeong, "말년·총운")]:
        g, gname = grade(val)
        out[key] = {"수": val, "등급": g, "격": gname, "뜻": kr, "길": is_gil(val)}
    out["_모두길"] = all(out[k]["길"] for k in ("원격", "정격"))
    out["_길개수"] = sum(out[k]["길"] for k in ("원격", "정격"))
    return out


