# -*- coding: utf-8 -*-
"""
리포트 프롬프트 빌더

report_generator가 만든 '뼈대(팩트 dict)' → LLM 프롬프트 문자열.
표준 톤·구조·금지사항을 프롬프트에 강제로 박아 항상 같은 품질을 보장.

흐름:  생년월일 → generate_report_data(뼈대) → build_prompt(프롬프트) → LLM → 3천자 리포트
"""
import json
from report_generator import generate_report_data
from datetime import datetime


STYLE_RULES = """[작성 규칙 — 반드시 지킬 것]
1. 분량: 3,000자 내외로 충분히 길고 풍성하게. 짧으면 안 됨(돈 낸 사람이 "이게 다야?" 하면 실패).
2. 언어: 옛 용어(관성·재성·생문 등)를 그대로 쓰지 말고 100% 현대 생활어로 번역.
   - 대운 → "인생의 시즌/계절", 겁재 → "승부욕·갓생 기질" 처럼.
   - 필요하면 요즘 개념(MBTI·FOMO·N잡 등)에 빗대도 좋음.
3. 라벨로 끊지 말 것. "때: 10월 / 방향: 서북" 같은 딱딱한 나열 금지.
   반드시 말로 풀어쓰기: "열두 달 훑어봤는데 10월에 확 몰리더라~"
4. 절대 금지: 특정 지역·동네 단정(방향까지만), 특정 직업 단정("OO직이군요" 금지 →
   "이런 기질이 강하네"로). 성향으로 열어두고 유저가 대입하게.
5. 캐릭터가 반말로 다정하게, 이야기하듯. 유저를 '너'로 부름.
6. 개인화 필수: 아래 '사주 스타일'을 반영해 그 사람한테만 맞는 조언.
   ("협력해라" 같은 뻔한 남 얘기 X → 사주 캐릭터에 맞춘 조언)
7. 기문 8요소(방위·성격·기운·변수·삼기·주도권·헛수고구간·이동수)를 문장 속에 자연스럽게 녹일 것.
   방위 하나만 쓰지 말고 전부 활용해 입체적으로.
8. 마지막은 반드시 캐릭터의 '응원일화'로 뭉클하게 마무리.
9. 아래 주어진 근거(사주·기문) 안에서만 서술. 없는 궁·별·기운을 지어내지 말 것.
10. ★엠대시(—, 대시, 하이픈 줄표) 절대 쓰지 말 것. 문장은 마침표·쉼표로만 끊는다. 대신 쉼표나 새 문장으로.
11. ★이 리포트는 위 근거에 적힌 '분야' 전용이다. 그 분야가 '종합 사주풀이'가 아닌 이상, ②'너는 이런 사람' 총론은 2~3문장으로만 짧게 하고, 나머지는 전부 그 분야(재물이면 돈, 연애면 인연, 직업이면 일 등) 얘기로 채워라. 다른 분야 리포트와 겹치지 않게, 그 분야에서만 나올 법한 구체적 상황·행동·조언을 많이 넣어라.
"""

STRUCTURE = """[리포트 구조]
① 캐릭터 인사 (누군지 한 줄 소개 포함)
② 너는 이런 사람이다 (사주 일간·오행·스타일 → 현대어로 성향 풀이)
③ 지금 네 판/시즌 (대운으로 큰 흐름)
④ 그래서 언제 (기문 최적월 — 말로 풀어서)
⑤ 어떤 성격의 일로/어디서 (기문 8요소 입체 해석)
⑥ 너답게 어떻게 (사주 스타일 반영 개인화 조언)
⑦ 조심할 것 (헛수고 구간 등)
⑧ 🪭 캐릭터 응원 (응원일화로 마무리)
"""


def build_prompt(data):
    """뼈대 dict → LLM 프롬프트."""
    saju = data["사주"]
    qm = data["기문"]
    med = "\n[특별] 건강 분야 — 리포트 맨 끝에 '실제 증상은 병원 진료를 받으세요' 고지 필수." if data["의료고지"] else ""

    facts = f"""[이 사람의 계산된 근거 — 여기 안에서만 서술]
· 분야: {data['분야']}
· 담당 캐릭터: {data['캐릭터']} ({data['캐릭터소개']})
· 응원에 쓸 일화: {data['응원일화']}
· 리포트 초점(이걸 답해줘): {data.get('초점','')}
· 캐릭터 말투(반드시 이 톤으로): {data.get('말투','')}
· 이 분야에서 특히 살릴 기문 요소: {data.get('강조','')}
· 분야 특화 조언 방향: {data.get('특화조언','')}
· 일간(본인): {saju['일간']} ({saju['일간오행']} 기운)
· 타고난 스타일: {saju['스타일키워드']} — {saju['스타일방식']}
· 오행 분포: {saju['오행분포']} (없는 기운: {', '.join(saju['없는오행']) or '없음'})
· 현재 대운(인생 시즌): {saju['대운']} / 기간 {saju['대운기간']}
· 올해({data['타겟연도']}) 기문 최적 시기: {qm['최적월'][0]}월 (그다음 {', '.join(str(m)+'월' for m in qm['최적월'][1:])})
· 기문 8요소 (대표월 {qm['대표월']}월 기준):
"""
    for k, v in qm["요소"].items():
        facts += f"   - {k}: {v}\n"

    return f"""당신은 '{data['캐릭터']}' 캐릭터로 빙의해 사주+기문 운세 리포트를 쓰는 작가입니다.
아래 근거로, 규칙과 구조에 맞춰 {data['분야']} 리포트를 작성하세요.

{facts}
{STYLE_RULES}
{STRUCTURE}{med}

이제 {data['캐릭터']}가 되어 리포트를 작성하세요. 인사부터 응원 마무리까지, 3천자 내외로."""


def make_full_prompt(dt_birth, gender, field_key, target_year=None):
    data = generate_report_data(dt_birth, gender, field_key, target_year)
    return build_prompt(data), data


if __name__ == "__main__":
    prompt, data = make_full_prompt(datetime(1991,3,21,19,56), "F", "wealth")
