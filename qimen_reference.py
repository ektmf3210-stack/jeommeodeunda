def summarize_cell(gung, digan, cheonban, gusung, bamen, bashen):
    """한 궁의 요소들을 근거 텍스트로 요약(LLM 프롬프트에 삽입용)."""
    lines = []
    g = GUNG_MEANING.get(gung, {})
    lines.append(f"[{gung}] 방위 {g.get('방위','-')}, 궁 오행 {g.get('오행','-')} — {g.get('상의','')}")
    if bamen and bamen in BAMEN_MEANING:
        m = BAMEN_MEANING[bamen]
        lines.append(f"  · 팔문 {bamen}({LABEL_KR.get(bamen,'')}) [{m['길흉']}] — {m['상의']}")
    if gusung and gusung in GUSUNG_MEANING:
        m = GUSUNG_MEANING[gusung]
        lines.append(f"  · 구성 {gusung}({LABEL_KR.get(gusung,'')}) [{m['길흉']}] — {m['상의']}")
    if bashen and bashen in BASHEN_MEANING:
        m = BASHEN_MEANING[bashen]
        lines.append(f"  · 팔신 {bashen}({LABEL_KR.get(bashen,'')}) [{m['길흉']}] — {m['상의']}")
    if cheonban and cheonban in GAN_MEANING:
        m = GAN_MEANING[cheonban]
        lines.append(f"  · 천반간 {cheonban}({LABEL_KR.get(cheonban,'')}) {m['분류']} — {m['상의']}")
    if digan and digan in GAN_MEANING:
        m = GAN_MEANING[digan]
        lines.append(f"  · 지반간 {digan}({LABEL_KR.get(digan,'')}) {m['분류']} — {m['상의']}")
    return "\n".join(lines)
